    private int odrediId() {
        try {
            KlijentTransfer kt = new KlijentTransfer();
            kt.setOperacija(Konstante.VRATI_REZERVACIJE);
            Komunikacija.getInstance().sendRequest(kt);
            ServerTransfer st = Komunikacija.getInstance().readResponse();
            List <Rezervacija> listrez = (List<Rezervacija>)st.getPodaci();
            int id=listrez.size()+1;
            return id;
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(DodajRezervacijuPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        return 0;
    }

