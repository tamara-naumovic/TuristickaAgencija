    private void srediFormu() {
        jcbAranzman.removeAllItems();
        List<Aranzman> lista = vratiAranzmane();
        for (Aranzman ar : lista) {
            jcbAranzman.addItem(ar);
        }
        jcbAranzman.setSelectedIndex(-1);
        jtxtDatumRezervisanja.setEnabled(false);
        jtxtRokPlacanja.setEnabled(false);
        jtxtZaduzenje.setEnabled(false);
    }
    private List<Aranzman> vratiAranzmane(){
            List<Aranzman> listaar = new LinkedList<>();
        try {
            
            listaar = Kontroler.Kontroler.getInstance().getAranzmane();
        } catch (Exception ex) {
            Logger.getLogger(DodajRezervacijuPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        return listaar;
                
    }

    private boolean dodajRezervaciju(Rezervacija r) {
        try {
            boolean kreiran= Kontroler.Kontroler.getInstance().kreirajRezervaciju(r);
            
            if (kreiran == true) {
                JOptionPane.showMessageDialog(this, "Uspesno kreirana rezervacija za klijenta: "+r.getKlijent().getIme()+" "+r.getKlijent().getPrezime());
                return true;
            }
        } catch (Exception ex) {
            Logger.getLogger(DodajRezervacijuPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }

    private boolean validirajFormu() {
        boolean valid =true;
        if (jtxtAvans.getText().isEmpty()) {
            jtxtAvans.setBorder(BorderFactory.createLineBorder(Color.RED));
            valid=false;
        }
        return valid;
    }
