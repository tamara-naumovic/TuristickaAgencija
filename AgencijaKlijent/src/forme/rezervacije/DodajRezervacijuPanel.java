    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!validirajFormu()) {
            JOptionPane.showMessageDialog(this, "Ne moze se kreirati rezervacija");
            return;
        }
        r = new Rezervacija();
        r.setKlijent(k);
        Aranzman ar = (Aranzman) jcbAranzman.getSelectedItem();
        Calendar cal = Calendar.getInstance();
        cal.setTime(ar.getDatumPolaska());
        cal.add(Calendar.DAY_OF_YEAR,-1);
        jtxtRokPlacanja.setText((new java.sql.Date((cal.getTime()).getTime())).toString());
        jtxtDatumRezervisanja.setText((new java.sql.Date((new Date()).getTime()).toString()));
        r.setAranzaman(ar);
        
        r.setDatum(new java.sql.Date((new Date()).getTime()));
        r.setRokPlacanja(new java.sql.Date((cal.getTime()).getTime()));
        
        try {
            Double avans = Double.parseDouble(jtxtAvans.getText().trim());
            r.setAvans(avans);
            r.setZaduzenje(r.getAranzaman().getCena()-r.getAvans());
            jtxtZaduzenje.setText(""+r.getZaduzenje());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Unesite broj!");
        }
        
        r.setRezervacijaID(odrediId());
        r.setAgent((Agent) Sesija.vratiInstancu().getMapa().get("agent"));
        if (dodajRezervaciju(r)==true) {
            Window w = SwingUtilities.getWindowAncestor(this);
            w.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(this, "Neuspesno kreiranje rezervacije");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

